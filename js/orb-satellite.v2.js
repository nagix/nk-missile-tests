Orb.RoundAngle = Orb.RoundAngle || function (degree) {
  var angle = degree % 360
  if (angle < 0) {
    angle = angle + 360
  }
  return angle;
}

Orb.ZeroFill = function (num, length) {
  if (length) {
    var length = length;
  } else {
    var length = num.length;
  }
  var seed = "0000000000"
  var str = seed + String(num)
  return str.slice(0 - length)
}
